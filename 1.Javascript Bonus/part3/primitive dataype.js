// Primitive Data Types:

// Number: Represents numeric values, both integers and floating-point numbers (e.g., 42, 3.14).
// String: Represents text data enclosed in single ('') or double ("") quotes (e.g., 'Hello, World!').
// Boolean: Represents either true or false, often used for conditional logic.
// Undefined: Represents a variable that has been declared but has not been assigned a value.
// Null: Represents the intentional absence of any object value.
// Symbol (ES6): Represents a unique and immutable value, primarily used as object property keys.
// BigInt (ES11): Represents large integers, useful for handling extremely large numbers.

var val1 = "New World"; //returns String
var val2 = 5; //returns Number
var val3 = true; //returns Boolean
var val4 = [1, 2, 3]; //returns Object
var val5 = null; //returns Object (Value is null, but type is still an object)
var val6; //returns Undefined
var val7 = BigInt(Number.MAX_SAFE_INTEGER); // 9007199254740991n
var val8 = Number.MAX_SAFE_INTEGER + 1;
